package S02.ex1;

import java.sql.SQLOutput;

public class Data {

    private int mes;
    private int dia;
    private int ano;

    public Data(int dia, int mes, int ano) {
        this.dia = dia;
        this.mes = mes;
        this.ano = ano;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public int getDia() {
        return dia;
    }

    public void setDia(int dia) {
        this.dia = dia;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public void exibirData() {
        String formatted = dia + "/" + mes + "/" + ano;
        System.out.println(formatted);
    }
}
